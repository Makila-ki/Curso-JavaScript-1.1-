let a = 10;

// relacionales
console.log(a > 5); // true
console.log(a < 5); // false
console.log(a >= 5); // true
console.log(a <= 5); // false

// igualdad
console.log(a == 10); // true
console.log(a != 10); // false
console.log(a == '10'); // true: compara el valor, no el tipo de dato.
console.log(a === '10'); // false: compara el valor y el tipo de dato.
console.log(a !== '10'); // true: compara el valor y el tipo de dato.